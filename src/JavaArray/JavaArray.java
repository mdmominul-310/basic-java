package JavaArray;

public class JavaArray {
    public static void main(String[] args){
        int[] a ={1,3,3,4,5};
        System.out.println("total array length is "+a.length);
        for(int i=0;i<a.length;i++){
            System.out.println("index is"+i);
            System.out.println(a[i]);
        }
    }
};


